class Animal{
	void sound() {
		System.out.println("This is Animal class");
	}
}
class Dog extends Animal{
	void sound1() {
		System.out.println("Dog Barks");
	}
}
class Cat extends Animal{
	void sound2() {
		System.out.println("Cats meows");
	}
}
class Cow extends Animal{
	void sound3() {
		System.out.println("Cow Moos");
	}
}